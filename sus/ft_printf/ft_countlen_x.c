#include "ft_printf.h"

int	ft_countlen_x(unsigned long nmb)
{
	int	i;

	i = 0;
	if (nmb < 0 || nmb == 0)
		i++;
	while (nmb != 0)
	{
		nmb /= 16;
		i++;
	}
	return (i);
}
