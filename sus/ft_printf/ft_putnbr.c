#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int	i;

	i = ft_countlen(n);
	if (n == -2147483648)
		return (write(1, "-2147483648", 11), 11);
	else if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar(n + '0');
	return (i);
}
