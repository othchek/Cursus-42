#include "ft_printf.h"

int	ft_countlen(long int nmb)
{
	int	i;

	i = 0;
	if (nmb <= 0)
		i++;
	while (nmb != 0)
	{
		nmb /= 10;
		i++;
	}
	return (i);
}
